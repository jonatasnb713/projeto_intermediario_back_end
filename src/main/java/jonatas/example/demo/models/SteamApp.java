package jonatas.example.demo.models;

public class SteamApp {
    private int appid;
    private String name;

    // Getters e Setters
    public int getAppid() {
        return appid;
    }

    public void setAppid(int appid) {
        this.appid = appid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
